def build_chunk_group_facts_prompt(chunks: list[str]) -> str:
    chunks_text = "\n\n".join(
        f"[ФРАГМЕНТ {i + 1}]\n{chunk}"
        for i, chunk in enumerate(chunks)
    )

    return f"""
Ты анализируешь несколько фрагментов одного документа на русском языке.

ВАЖНО:
- Отвечай строго только на русском языке.
- Не используй английский язык.
- Не выдумывай факты.
- Извлекай только то, что явно есть в тексте.
- Удали повторы.
- Формулируй факты коротко и конкретно.
- Если сущностей нет, верни пустой список.

Верни ответ строго в формате:

FACTS:
- <факт 1>
- <факт 2>
- <факт 3>

ENTITIES:
- <сущность 1>
- <сущность 2>
- <сущность 3>

Фрагменты документа:
{chunks_text}
""".strip()


def build_document_aggregation_prompt(
    facts: list[str],
    entities: list[str],
    source: str,
) -> str:
    facts_text = "\n".join(f"- {fact}" for fact in facts) if facts else "-"
    entities_text = "\n".join(f"- {entity}" for entity in entities) if entities else "-"

    return f"""
Ты анализируешь извлечённые факты по документу на русском языке.

Имя файла:
{source}

ВАЖНО:
- Отвечай строго только на русском языке.
- Не используй английский язык.
- Не выдумывай информацию, которой нет во входных фактах и сущностях.
- Сформируй короткое и точное описание документа.
- Удали повторы и объедини одинаковые факты.
- Если можно определить тип документа, укажи его одним словом:
  устав / договор / приказ / положение / инструкция / регламент / неизвестно

Верни ответ строго в формате:

SUMMARY:
<краткое содержание документа на русском языке, 3-5 предложений>

FACTS:
- <ключевой факт 1>
- <ключевой факт 2>
- <ключевой факт 3>

ENTITIES:
- <ключевая сущность 1>
- <ключевая сущность 2>
- <ключевая сущность 3>

DOCUMENT_TYPE:
<тип документа>

Факты:
{facts_text}

Сущности:
{entities_text}
""".strip()